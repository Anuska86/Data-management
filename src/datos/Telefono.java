package datos;

public class Telefono {

	
	private String num;
	private String prefijo;

	public Telefono () {
		
	}
	
	public String getNum() {
		return num;
	}

	public String getPrefijo() {
		return prefijo;

	}

	public void setNum(String num) {
		this.num =num;

	}

	public void setPrefijo(String prefijo) {
		this.prefijo = prefijo;

	}

}

